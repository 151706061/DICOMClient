    /**
     * Convert an array of integers into an icon.
     *
     * @param array Integers representing an icon.
     *
     * @return A displayable icon.
     */
    private static ImageIcon ConvertIntsToIcon(short array[]) {
        byte bytePng[] = new byte[array.length];
        for (int i = 0; i < array.length; i++) {
            bytePng[i] = (byte)(array[i]);
        }
        return new ImageIcon(bytePng);
    }

    
    /**
     * Get the icon indicating that a series has been uploaded.
     * 
     * @return A happy icon.
     */
    public static synchronized ImageIcon getOk() {
        if (okIcon == null) {
            okIcon = ConvertIntsToIcon(ok);
        }
        return okIcon;
    }
    
    /**
     * Get the icon indicating that a series has not been uploaded.
     * 
     * @return A blank icon.
     */
    public static synchronized ImageIcon getEmpty() {
        if (emptyIcon == null) {
            emptyIcon = ConvertIntsToIcon(empty);
        }
        return emptyIcon;
    }
